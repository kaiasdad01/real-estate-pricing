## Data Sources and Coverage

Our market data is compiled by aggregating our internal [property listing records](https://developers.rentcast.io/reference/property-listings) and removing outlying data spikes to improve overall statistical accuracy.

Market data for the current month is updated once per day. At the end of each month, we take a snapshot of the aggregate data for that month and save it as a historical record for each zip code.

Similar to our `/listings` endpoints, our aggregate market data takes into account around 96% of sale and rental listings for residential properties in all 50 US states, and around 90% of sale and rental listings for 5+ unit commercial apartment buildings, condo complexes and other residential developments.

